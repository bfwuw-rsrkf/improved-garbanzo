class data_types {
    public static void main(String[] args) {
        int whole_number = 4;
        double fractional_number = 4.0;
        boolean am_I_gonna_ace_this_class = true;
        char single_character = 'A';
        String fact = "Programming is super interesting";

        System.out.println(whole_number);
        System.out.println(fractional_number);
        System.out.println(am_I_gonna_ace_this_class);
        System.out.println(single_character);
        System.out.println(fact);

        int x; // declared but not initialized/given a value
        x = 123;
        System.out.println(x);
    }
}
